# Proficiency tribble manipulation
def hi_tribble(byte: int) -> int:
    return (byte >> 3) & 0x07

def lo_tribble(byte: int) -> int:
    return byte & 0x07

def make_tribble(lo: int, hi: int) -> int:
    return (hi << 3) | (lo & 0x07)

